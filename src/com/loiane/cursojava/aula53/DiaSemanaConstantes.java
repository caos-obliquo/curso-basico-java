package com.loiane.cursojava.aula53;

public class DiaSemanaConstantes {

    // Exemplo de como representar dias de semana usando CONSTANTES em Java.
    // * Constantes são definidas como 'public static final' (imutáveis e acessíveis globalmente).]
    // * Desvantagem: não há type safety (o compilador não previna valores inválidos, como '8' por exemplo nesse caso).
    public static final int SEGUNDA = 1;
    public static final int TERCA = 2;
    public static final int QUARTA = 3;
    public static final int QUINTA = 4;
    public static final int SEXTA = 5;
    public static final int SABADO = 6;
    public static final int DOMINGO = 7;
}
